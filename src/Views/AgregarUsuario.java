    public void agregarUsuario(){
        if(!txtUser.getText().equals("") || !txtPass.getText().equals("")){
            BalAlumnos alumno = new BalAlumnos();
            alumno.asignarUsuario(Integer.parseInt(txtNoControl.getText()), txtUser.getText(), txtPass.getText()); 
        }else{
            JOptionPane.showMessageDialog(null, "No campos vacios");
        }
    }

